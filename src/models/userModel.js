// =============================
// USER MODEL - نموذج المستخدم
// =============================

// استيراد مكتبة mongoose للتعامل مع قاعدة بيانات MongoDB
import mongoose from "mongoose"


// =====================================
// USER SCHEMA - مخطط بيانات المستخدم
// =====================================

// هنا نحدد شكل البيانات التي سيتم تخزينها في قاعدة البيانات
const userSchema = new mongoose.Schema(
{

  // -----------------------------------
  // NAME FIELD - حقل اسم المستخدم
  // -----------------------------------
  name: {
    type: String,       // نوع البيانات نص
    required: true,     // هذا الحقل إجباري
    trim: true          // يحذف الفراغات الزائدة
  },


  // -----------------------------------
  // EMAIL FIELD - حقل البريد الإلكتروني
  // -----------------------------------
  email: {
    type: String,       // نوع البيانات نص
    required: true,     // الحقل إجباري
    unique: true,       // لا يمكن تكرار نفس الإيميل
    lowercase: true     // يحول الإيميل إلى حروف صغيرة
  },


  // -----------------------------------
  // PASSWORD FIELD - حقل كلمة المرور
  // -----------------------------------
  password: {
    type: String,       // نوع البيانات نص
    default: ""         // القيمة الافتراضية فارغة (لأن Google Login لا يحتاجها)
  },


  // -----------------------------------
  // FIREBASE UID - معرف Firebase
  // -----------------------------------
  // يستخدم فقط عند تسجيل الدخول عبر Google
  firebaseUid: {
    type: String,
    default: null       // ليس إجباري
  },


  // -----------------------------------
  // USER ROLE - دور المستخدم
  // -----------------------------------
  role: {
    type: String,                   // نوع البيانات نص
    enum: ["user", "admin"],        // القيم المسموحة
    default: "user"                 // القيمة الافتراضية
  },


  // -----------------------------------
  // FCM TOKEN - توكن الإشعارات
  // -----------------------------------
  // سيستخدم لاحقاً للإشعارات
  fcmToken: {
    type: String,
    default: null
  }

},

// =====================================
// TIMESTAMPS - وقت الإنشاء والتعديل
// =====================================
{
  timestamps: true   // يضيف createdAt و updatedAt تلقائياً
}

)


// =====================================
// USER MODEL - إنشاء الموديل
// =====================================

// هنا نحول المخطط إلى موديل يمكن استخدامه في المشروع
const User = mongoose.model("User", userSchema)


// =====================================
// EXPORT MODEL - تصدير الموديل
// =====================================

// تصدير الموديل حتى نستخدمه في controllers
export default User